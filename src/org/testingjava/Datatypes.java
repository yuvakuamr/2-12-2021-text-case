package org.testingjava;

public class Datatypes {
	
	
	public static void main(String[] args) {
		
		
		//primitive type
		
		
		byte id = 12;
		
		short rollNum = 1234;
		
		int accnum = 85258525;
		
		long mblNum = 9500687425l;
		
		float saraly = 25.46373738f;
		
		double takeSal = 19.7348373983;
		
		char block = 'b';
		
		boolean value = true;
		
		//non primitive type
		
		String name = "yuva";
	
		System.out.println("My Name : "+name);
		
		System.out.println("My id : "+id );
		
		System.out.println("My Roll Number : "+ rollNum);
		
		System.out.println("My Account Number : "+ accnum);
		
		System.out.println("My Mobile Number : "+ mblNum);
		
		System.out.println("My Salary : "+ saraly);
		
		System.out.println("My Take Home Salary : "+ takeSal);
		
		System.out.println("My Block : "+block);

		System.out.println("This Document also : "+value );
		
		
		
		
	}

}
